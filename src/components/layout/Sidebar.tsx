type NavLink = { label: string; href: string; icon: React.FC<{ className?: string }> };
type NavHeading = { heading: string };
type NavItem = NavLink | NavHeading;

function isHeading(item: NavItem): item is NavHeading {
  return "heading" in item;
}

export default function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="flex w-64 flex-col border-r border-gray-200 bg-white">
      <div className="flex h-16 items-center border-b border-gray-200 px-4">
        <Image src="/logo.png" alt="Cart to Close" width={180} height={48} className="h-10 w-auto" priority />
      </div>

      <nav className="flex-1 overflow-y-auto px-3 py-4">
        <ul className="space-y-1">
          {(NAV_ITEMS as unknown as NavItem[]).map((item, i) => {
            if (isHeading(item)) {
              return (
                <li key={i} className="px-3 pb-1 pt-5 text-xs font-semibold uppercase tracking-wider text-gray-400">
                  {item.heading}
                </li>
              );
            }

            const active = pathname === item.href || pathname.startsWith(item.href + "/");
            const Icon = item.icon;

            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                    active
                      ? "bg-blue-50 text-blue-700"
                      : "text-gray-700 hover:bg-gray-100 hover:text-gray-900"
                  }`}
                >
                  <Icon className={`h-5 w-5 ${active ? "text-blue-600" : "text-gray-400"}`} />
                  {item.label}
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      <div className="border-t border-gray-200 px-3 py-4">
        <button
          onClick={() => signOut({ callbackUrl: "/login" })}
          className="flex w-full items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium text-gray-700 hover:bg-gray-100 hover:text-gray-900 transition-colors"
        >
          <LogOutIcon className="h-5 w-5 text-gray-400" />
          Sign Out
        </button>
      </div>
    </aside>
  );
}

